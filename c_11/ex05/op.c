int	op_plus(int a, int b)
{
	return (a + b);
}

int	op_minus(int a, int b)
{
	return (a - b);
}

int	op_division(int a, int b)
{
	return (a / b);
}

int	op_modulo(int a, int b)
{
	return (a % b);
}

int	op_mul(int a, int b)
{
	return (a * b);
}
